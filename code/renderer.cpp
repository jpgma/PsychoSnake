#include <stdio.h>
#include <windows.h>
#define comprimento 20
#define altura 10

void LimparTela()
{
	char bitmap [altura*comprimento];
	for(int i=1;i<=altura;i++)
	{
		for(int j=1;j<=comprimento;j++)
		{	
			bitmap[i*j] = -80;
		}
	}
}


void PrintarBitMap()
{
	char bitmap [altura*comprimento];
	for(int i=1;i<=altura; i++)
	{
		for(int j=1; j<=comprimento; j++)
		{
			printf("%c", bitmap[i*j]);
		}
		printf("\n");
	}
}
		
